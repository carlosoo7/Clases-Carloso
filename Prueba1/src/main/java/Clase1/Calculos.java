package Clase1;

public class Calculos {

    private int numero1, numero2;

    public double sumar(double suma1, double suma2){
        return suma1+suma2;
    }
    public double restar(double resta1, double resta2){
        return resta1-resta2;
    }
    public double multiplicar(double multiplicador1, double multiplicador2){
        return multiplicador1*multiplicador2;
    }
    public double dividir(double divisor1, double divisor2){
        return divisor1/divisor2;
    }


}
